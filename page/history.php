		<h4><b>1983/84</b></h4>
		<p>Nanyang Technological Institute Buddhist Society (NTIBS) was officially gazette by Registry Of Society (ROS).</p>
		<p>A Bodhi Tree sapling donated by Mangala Vihara was planted outside Nanyang House by Mr. Yeo Choo Kok on the same day. The Bodhi tree sapling was from its parent tree which is a branch from the very tree Lord Shakyamuni Buddha sat under upon Enlightenment.</p>
		<p>NTIBS was voted the Best Society in a survey conducted by the DSA.</p>
